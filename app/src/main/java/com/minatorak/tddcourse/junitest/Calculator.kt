package com.minatorak.tddcourse.junitest

class Calculator {

    fun add(i: Int, i1: Int): Int {
        return i+i1
    }

    fun subtract(i: Int, i1: Int): Int {
        return i-i1
    }

    fun multiply(i: Int, i1: Int): Int {
        return i*i1
    }

    fun divide(i: Int, i1: Int): Int {
        return i/i1
    }

}
